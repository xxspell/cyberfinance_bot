import random
import string
import secrets
import hashlib


class Utils:
    def generate_random_hash(length):
        # Генерируем случайную строку нужной длины
        random_string = secrets.token_hex(length // 2)  # Длина хеша в байтах, поэтому делим длину на 2
        # Создаем хеш SHA-256 для случайной строки
        hash_value = hashlib.sha256(random_string.encode()).hexdigest()
        return hash_value

    def generate_random_string(length):
        # Символы, которые будут использоваться при генерации строки
        characters = string.ascii_letters + string.digits
        # Генерация случайной строки
        return ''.join(random.choice(characters) for i in range(length))



